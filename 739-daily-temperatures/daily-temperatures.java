class Solution {
    public int[] dailyTemperatures(int[] temperatures) {
        int n = temperatures.length;
        int[] res = new int[n];
        Stack<Integer> st = new Stack<>();
        res[n - 1] = 0;
        st.push(n-1);
        for(int i = n - 2; i >= 0; i--){
            while(!st.empty() && temperatures[st.peek()] <= temperatures[i]){
                st.pop();
            }
            if(st.empty()){
                res[i] = 0;
                st.push(i);
            } else{
                res[i] = st.peek() - i;
                st.push(i);
            }
        }
        return res;
    }
}